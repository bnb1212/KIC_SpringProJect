package pack.model;

public class CurriDto {
	private String curri_no, class_no, section_no, goal_title;

	public String getCurri_no() {
		return curri_no;
	}

	public void setCurri_no(String curri_no) {
		this.curri_no = curri_no;
	}

	public String getClass_no() {
		return class_no;
	}

	public void setClass_no(String class_no) {
		this.class_no = class_no;
	}

	public String getSection_no() {
		return section_no;
	}

	public void setSection_no(String section_no) {
		this.section_no = section_no;
	}

	public String getGoal_title() {
		return goal_title;
	}

	public void setGoal_title(String goal_title) {
		this.goal_title = goal_title;
	}
	
}
